#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int i;

	i = -1;
	if (n < 0)
	{
		write(fd, "-", 1);
		i = 1;
	}
	while (n / i <= -10)
		i *= 10;
	while (i)
	{
		ft_putchar_fd(-(n / i) + 48, fd);
		n = n % i;
		i /= 10;
	}
}
