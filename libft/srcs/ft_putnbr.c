#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	i = -1;
	if (n < 0)
	{
		write(1, "-", 1);
		i = 1;
	}
	while (n / i <= -10)
		i *= 10;
	while (i)
	{
		ft_putchar(-(n / i) + 48);
		n = n % i;
		i /= 10;
	}
}
