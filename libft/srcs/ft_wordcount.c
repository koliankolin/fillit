int	ft_wordcount(const char *str, char c)
{
	int			i;

	i = 0;
	while (*str)
	{
		while (*str == c)
			str++;
		if (*str != c && *str)
		{
			i++;
			while (*str != c && *str)
				str++;
		}
	}
	return (i);
}
